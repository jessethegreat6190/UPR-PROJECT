function getDistricts() {
    return UGANDA_DISTRICTS.map(d => `<option value="${d.code}">${d.name}</option>`).join('');
}

function addDistrictDropdown(elementId, label = "District") {
    const el = document.getElementById(elementId);
    if (!el) return;
    el.innerHTML = `<div class="form-group">
        <label>${label} *</label>
        <select id="${elementId}_select" required>
            <option value="">Select District</option>
            ${getDistricts()}
        </select>
    </div>`;
}